-- create a table
CREATE TABLE COUNTRIES (
  COUNTRY_CDE varchar(10),
  NAME varchar(20),
  DESCRIPTION varchar(255),
  IS0_NUM_CODE varchar(10),
  PRIMARY KEY (COUNTRY_CDE)
);
CREATE TABLE TXN_ACCOUNTS (
  INTERNAL_ID int(10),
  ID int(20),
  NAME varchar(255),
  CURRENCY_CDE varchar(10),
  COUNTRY_CDE varchar(10),
  ACC_BAL int(20),
  ACC_TYPE varchar(10),
  CREATED_BY varchar(20),
  CREATED_DATE timestamp,
  LAST_MODIFIED_BY varchar(20),
  LAST_MODIFIED_DATE varchar timestamp,
  PRIMARY KEY (INTERNAL_ID),
  foreign KEY (COUNTRY_CDE) references COUNTRIES (COUNTRY_CDE),
  foreign KEY (CURRENCY_CDE) references CURRENCIES (CURRENCY_CDE)
);
CREATE TABLE TXN_TYPES (
  TXN_TYPE_CDE varchar(10),
  NAME varchar(20),
  DESCRIPTION varchar(255),
  PRIMARY KEY (TXN_TYPE_CDE)
);
CREATE TABLE TRANSACTIONS (
  INTERNAL_ID INT(10),
  ID INT(20),
  TXN_ACC_INTERNAL_ID INT(255),
  TXN_CURRENCY_CDE varchar(10),
  TXN_TYPE varchar(10),
  TXN_AMOUNT INT(20),
  TXN_STATUS varchar(10),
  CREATED_BY varchar(20),
  CREATED_DATE timestamp,
  LAST_MODIFIED_BY varchar(20),
  LAST_MODIFIED_DATE timestamp,
  PRIMARY KEY (INTERNAL_ID),
  foreign KEY (COUNTRY_CDE) references COUNTRIES (COUNTRY_CDE),
  foreign KEY (CURRENCY_CDE) references CURRENCIES (CURRENCY_CDE)
);
CREATE TABLE CURRENCIES (
  CURRENCY_CDE varchar(10),
  NAME varchar(20),
  DESCRIPTION varchar(255),
  IS0_NUM_CODE varchar(10),
  PRIMARY KEY (CURRENCY_CDE)
);
